int main(void)
{
  int read;
  int32_t count = 0;
 
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_ADC1_Init();
  MX_USB_DEVICE_Init();
  
  while (1)
  {
      HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1,100);
    read = HAL_ADC_GetValue(&hadc1);
    if (count >= 50)
    {
      sprintf(buffer, "Tesao no pino adc %d mV\r\n", (read*3300/4095));
      CDC_Transmit_FS(buffer,strlen(buffer));
      count = 0;
    }
    count++;

    HAL_Delay(10);
    if (gUSB[0]!=0)
    {
      switch(gUSB[0])
      {
        case 103:
        {
          sprintf(buffer, "Digitou G ne safada\r\n");
          CDC_Transmit_FS(buffer,strlen(buffer));
          break;
        }
        default:
        {
          break;
        }
      }
      memset(gUSB,0,255);
    }
  }
}

